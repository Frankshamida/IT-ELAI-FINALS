// Flatten FAQ for search
const allFAQs = Object.values(chatbotFAQ).flat();

// RAG: Retrieval-Augmented Generation
// Calculate relevance score based on keyword matching and semantic similarity
function calculateRelevanceScore(userQuery, faqItem) {
  const query = userQuery.toLowerCase().split(/\s+/);
  const keywords = faqItem.keywords || [];
  const question = faqItem.q.toLowerCase().split(/\s+/);
  const answer = faqItem.a.toLowerCase().split(/\s+/);
  
  let score = 0;
  
  // Keyword matching (highest weight)
  query.forEach(word => {
    if (keywords.some(kw => kw.includes(word) || word.includes(kw))) {
      score += 3;
    }
    if (question.some(w => w.includes(word) || word.includes(w))) {
      score += 2;
    }
    if (answer.some(w => w.includes(word) || word.includes(w))) {
      score += 1;
    }
  });
  
  // Word overlap in question
  const questionOverlap = question.filter(w => query.includes(w)).length;
  score += questionOverlap * 2;
  
  return score;
}

// RAG Search: Retrieve most relevant FAQ items
function searchFAQWithRAG(userQuery) {
  if (!userQuery.trim()) return [];
  
  // Score all FAQs
  const scored = allFAQs.map(item => ({
    ...item,
    relevanceScore: calculateRelevanceScore(userQuery, item)
  }));
  
  // Sort by relevance
  scored.sort((a, b) => b.relevanceScore - a.relevanceScore);
  
  // Return top match if score is above threshold
  if (scored[0].relevanceScore > 0) {
    return [scored[0]];
  }
  
  return [];
}

// Get context from multiple relevant FAQs (for better context)
function getRAGContext(userQuery, topK = 3) {
  if (!userQuery.trim()) return [];
  
  const scored = allFAQs.map(item => ({
    ...item,
    relevanceScore: calculateRelevanceScore(userQuery, item)
  }));
  
  scored.sort((a, b) => b.relevanceScore - a.relevanceScore);
  
  return scored.slice(0, topK).filter(item => item.relevanceScore > 0);
}

// Enhanced search function
function searchFAQ(query) {
  return searchFAQWithRAG(query);
}

// Get random FAQ
function getRandomFAQ() {
  return allFAQs[Math.floor(Math.random() * allFAQs.length)];
}
